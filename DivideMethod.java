public class DivideMethod {
    public static void main(String[] args) {       
        int num1 = 25;
        int num2 = 3;
       
       divide(num1, num2);       
    }
   
    public static void divide(int num1, int num2) {
        System.out.println(num1 + "/" + num2 + "=" + num1 * 1.0 / num2);
    
}
} 